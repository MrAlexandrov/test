#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

void solve() {
    ll u, v;
    cin >> u >> v;
    cout << - u * u << ' ' << v * v << '\n';
}

int main() {
    ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    ///freopen("input.in", "r", stdin);
    ///freopen("output.out", "w", stdout);
    ll T;
    cin >> T;
    while (T--) {
        solve();
    }
    return 0;
}

